public class LuasBalok3o2 {
    public static void main(String[] args) {
        int p=5;
        int l=10;
        int t=5;
        System.out.println("\nNilai Panjang Balok:" +p);
        System.out.println("\nNilai Lebar Balok:" +l);
        System.out.println("\nNilai tinggi Balok:" +t);
        System.out.println("\n===============================");
        System.out.println("\nMenghitung Luas Permukaan Balok");
        System.out.println("\n===============================");
        System.out.println("\nLuas Permukaan Balok= "+((2*p*l)+(2*p*t)+(2*l*t)));
    }
}
